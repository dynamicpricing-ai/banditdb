#!/usr/bin/env python3
"""
MovieLens Replay Evaluation
============================

Evaluates BanditDB's learned model against a random baseline using the
replay method on the held-out test set (movielens_test.jsonl).

The replay method (unbiased offline estimator)
-----------------------------------------------
For each logged interaction (arm_logged, context, reward):
  1. Ask BanditDB which arm to pick:  arm_predicted = db.predict(context)
  2. If arm_predicted == arm_logged:  count this event, use the logged reward
  3. If arm_predicted != arm_logged:  SKIP (don't update model, don't count)

This gives an unbiased estimate of BanditDB's policy value because the
MovieLens data was generated by real user choices — not a controlled policy.
Accepted events are those where BanditDB's choice agrees with the user's.

Prerequisites
-------------
  1. Run convert.py first to generate data/movielens_test.jsonl
  2. Load the train WAL into BanditDB and restart:
       docker cp benchmark/data/movielens_train.jsonl <container>:/data/bandit_wal.jsonl
       docker compose restart
  3. Set BANDITDB_URL and BANDITDB_API_KEY if needed

Usage
-----
  pip install banditdb-python
  python benchmark/movielens/evaluate.py
  python benchmark/movielens/evaluate.py --url http://localhost:8080 --api-key your-key
"""

import argparse
import json
import os
import sys
from pathlib import Path

try:
    from banditdb import Client, BanditDBError
except ImportError:
    sys.exit("Missing dependency — run:  pip install banditdb-python")

HERE     = Path(__file__).parent
TEST_WAL = HERE.parent / "data" / "movielens_test.jsonl"
CAMPAIGN = "movielens_recommendation"


def load_test_interactions(path: Path) -> list:
    """
    Parse the test WAL into a list of (arm_logged, context, reward) triples.
    Pairs Predicted and Rewarded events by interaction_id.
    """
    predicted = {}
    rewarded  = {}

    for line in path.read_text().splitlines():
        event = json.loads(line)
        if "Predicted" in event:
            p = event["Predicted"]
            predicted[p["interaction_id"]] = (p["arm_id"], p["context"])
        elif "Rewarded" in event:
            r = event["Rewarded"]
            rewarded[r["interaction_id"]] = r["reward"]

    interactions = []
    for iid, (arm, context) in predicted.items():
        if iid in rewarded:
            interactions.append((arm, context, rewarded[iid]))

    return interactions


def run_replay(db: Client, interactions: list, log_every: int = 1000) -> dict:
    """
    Run the replay evaluation and return statistics.
    """
    bandit_reward  = 0.0
    bandit_count   = 0
    random_reward  = 0.0
    random_count   = 0
    total          = len(interactions)

    # Random baseline: average reward across all logged events.
    # This is the CTR of the logging policy (real user behaviour).
    for _, _, reward in interactions:
        random_reward += reward
        random_count  += 1

    random_avg = random_reward / random_count if random_count else 0.0

    print(f"Running replay evaluation on {total:,} test interactions ...")
    print(f"Random baseline avg reward: {random_avg:.4f}  ({random_count:,} events)\n")

    for i, (arm_logged, context, reward) in enumerate(interactions, 1):
        try:
            arm_predicted, interaction_id = db.predict(CAMPAIGN, context)
        except BanditDBError as e:
            print(f"  [warn] predict failed at event {i}: {e}")
            continue

        if arm_predicted == arm_logged:
            # Replay match: use the logged reward and close the loop
            bandit_count  += 1
            bandit_reward += reward
            try:
                db.reward(interaction_id, reward)
            except BanditDBError as e:
                print(f"  [warn] reward failed at event {i}: {e}")
        else:
            # No match: discard this event (replay method requires this)
            # We still need to discard the interaction from BanditDB's cache
            # by sending reward=0 — but that would bias the model.
            # Instead we just let it expire via TTL.
            pass

        if i % log_every == 0:
            avg = bandit_reward / bandit_count if bandit_count else 0
            match_rate = bandit_count / i
            print(
                f"  [{i:>6,}/{total:,}]  matched={bandit_count:,}  "
                f"match_rate={match_rate:.1%}  avg_reward={avg:.4f}"
            )

    return {
        "bandit_count":  bandit_count,
        "bandit_avg":    bandit_reward / bandit_count if bandit_count else 0.0,
        "random_avg":    random_avg,
        "random_count":  random_count,
        "total_events":  total,
        "match_rate":    bandit_count / total if total else 0.0,
    }


def main():
    parser = argparse.ArgumentParser(description="MovieLens replay evaluation for BanditDB")
    parser.add_argument("--url",     default=os.environ.get("BANDITDB_URL", "http://localhost:8080"))
    parser.add_argument("--api-key", default=os.environ.get("BANDITDB_API_KEY"))
    args = parser.parse_args()

    if not TEST_WAL.exists():
        sys.exit(f"Test WAL not found: {TEST_WAL}\nRun convert.py first.")

    db = Client(url=args.url, api_key=args.api_key, timeout=5.0)

    # Verify server is up and campaign exists
    if not db.health():
        sys.exit(f"BanditDB at {args.url} is not reachable.")

    campaigns = {c["campaign_id"] for c in db.list_campaigns()}
    if CAMPAIGN not in campaigns:
        sys.exit(
            f"Campaign '{CAMPAIGN}' not found.\n"
            f"Load the train WAL and restart BanditDB first."
        )

    interactions = load_test_interactions(TEST_WAL)
    if not interactions:
        sys.exit("No test interactions found. Re-run convert.py.")

    stats = run_replay(db, interactions)

    # Results
    lift = (stats["bandit_avg"] / stats["random_avg"] - 1) * 100 if stats["random_avg"] else 0
    sep  = "=" * 58
    print(f"\n{sep}")
    print(f"  Replay Evaluation Results")
    print(sep)
    print(f"  Test interactions  : {stats['total_events']:>8,}")
    print(f"  Replay matches     : {stats['bandit_count']:>8,}  ({stats['match_rate']:.1%} match rate)")
    print(f"  BanditDB avg reward: {stats['bandit_avg']:>8.4f}")
    print(f"  Random   avg reward: {stats['random_avg']:>8.4f}")
    print(f"  Lift over random   : {lift:>+8.1f}%")
    print(sep)
    if lift > 0:
        print(f"  BanditDB outperforms random selection by {lift:.1f}%")
    else:
        print(f"  Model needs more training data to outperform random.")
    print()


if __name__ == "__main__":
    main()
